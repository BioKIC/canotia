								<div style="text-align: center;font-size:16px;margin-top:20px">
									<b>How to Cite Vascular Plants of Arizona Project</b>
								</div>
								<div style="text-align: justify;font-size:16px;margin-top:15px">
									Treatments for the Vascular Plants of Arizona Project can be cited
									collectively as follows:
								</div>
								<div style="text-align: justify;font-size:16px;margin-top:15px">
									VASCULAR PLANTS OF ARIZONA EDITORIAL COMMITTEE. 1992+. Vascular
									Plants of Arizona. <i>Journal of the Arizona-Nevada Academy of
									Science</i> and <i>Canotia</i> (all contributions are
									available at http://www.canotia.org/ vpa_project.html).
								</div>
								<div style="text-align:center;font-size:16px;margin-top:15px">
									<b><i>or</i></b>
								</div>
								<div style="text-justify;font-size:16px;margin-top:15px">
									Individual treatments of vascular plant families can be cited according to
									the journal in which they appeared. For example as:
								</div>
								<div style="text-align: justify;font-size:16px;margin-top:15px">
									CHIANG, F. and L. R. LANDRUM. 2009. Vascular Plants of Arizona:
									<i>Solanaceae</i> Part Three: <i>Lycium</i>. <i>Canotia</i>
									5(1): 17-26.
								</div>
								<div style="text-align: justify;font-size:16px;margin-top:15px">
									HABER, E. 1992. Vascular Plants of Arizona: Monotropaceae;
									Indian pipe family. <i>Journal of the Arizona-Nevada Academy of
										Science</i> 26(1): 15-6.
								</div>
