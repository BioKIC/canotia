								<div style="text-align: center;font-size:16px;margin-top:30px"><b>Vascular Plants of Arizona (VPA) Project</b></div>
								<p style="text-align: justify;font-size:16px;text-indent: 25px;">
									Arizona is a floristically rich
									state with perhaps as many as 3900 species of vascular plants.
									Over the last 60 years an average of ca. 12 new species
									records have been reported annually. The first manuals for
									identifying the plants of the state were <i>Flora of Arizona
									and New Mexico</i> by Tidestrom & Kittle (1941) and <i>Flowering
									Plants and Ferns of Arizona</i> by Kearney and Peebles
									(1942). When the latter, a government publication, was no
									longer available, the authors revised their book, and the
									University of California Press republished it under the title
									of <i>Arizona Flora</i> in 1951. A second edition appeared in
									1960 with a supplement provided by J.T. Howell, Elizabeth
									McClintock, and collaborators. These were followed by
									<i>A Catalogue of the Flora of Arizona</i>, a checklist of Arizona
									plants by J. Harry Lehr (1978), and two supplements to the
									catalogue were later published by Lehr and Pinkava (1980 and
									1982).
								</p>
								<p style="text-align: justify;font-size:16px;text-indent: 25px;">
									Since the latest manual is more
									than fifty years out-of-date and has no species descriptions,
									there is an urgent need for a new manual. In 1987, the
									botanists at the herbarium of Arizona State University and The
									University of Arizona formed an editorial committee with the
									intent of producing a new book - <i>Vascular Plants of</i> <i>Arizona</i>.
									Help was solicited from authorities in various taxonomic
									groups, and approximately 100 specialists were enrolled to
									contribute to the project. It soon became clear that obtaining
									completed manuscripts from all authors would take many years.
									Thus, in 1992 the Editorial Board began publishing completed
									treatments in the <i>Journal of the Arizona-Nevada Academy of
									Science</i> (JANAS), and seven issues, which included species
									descriptions, maps, and illustrations, were published in that
									journal. In 2005, with the initiation of a new Arizona
									journal, VPA treatments began to be published in <b><i>Canotia</i></b>.
									Several VPA treatments published in JANAS and all of those
									published in <b><i>Canotia</i></b> are available to all online
									here.
								</p>
								<hr>
